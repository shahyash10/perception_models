def tokenizer_image_token(prompt, tokenizer, image_token_index=IMAGE_TOKEN_INDEX, return_tensors=None):
    # Ensure the prompt is a structured conversation
    if isinstance(prompt, str):
        raise ValueError("Expected a structured conversation, but got a string.")

    # Tokenize each message in the conversation
    input_ids = []
    for message in prompt:
        if message["role"] == "user":
            input_ids.extend(tokenizer(message["content"]).input_ids)
        elif message["role"] == "assistant":
            input_ids.append(image_token_index)  # Add image token for assistant's response

    if return_tensors is not None:
        if return_tensors == 'pt':
            return torch.tensor(input_ids, dtype=torch.long)
        raise ValueError(f'Unsupported tensor type: {return_tensors}')
    return input_ids